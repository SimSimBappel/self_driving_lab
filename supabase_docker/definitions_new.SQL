create table
  public.aruco_tag (
    aruco_id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    name text null,
    constraint aruco_tag_pkey primary key (aruco_id)
  ) tablespace pg_default;

  create table
  public.chemical (
    chemical_id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    name text null,
    constraint chemical_pkey primary key (chemical_id),
    constraint chemical_name_key unique (name)
  ) tablespace pg_default;

  
create table
  public.chemical_placements (
    chemical_id bigint generated by default as identity,
    slot_id bigint not null,
    empty boolean null,
    created_at timestamp with time zone not null default now(),
    constraint chemical_placements_pkey primary key (chemical_id, slot_id),
    constraint public_chemical_placements_chemical_id_fkey foreign key (chemical_id) references chemical (chemical_id) on update cascade on delete cascade,
    constraint public_chemical_placements_slot_id_fkey foreign key (slot_id) references tray_slot (slot_id) on update cascade on delete cascade
  ) tablespace pg_default;

  create table
  public.machine (
    machine_id bigint generated by default as identity,
    workstation_id bigint null,
    created_at timestamp with time zone not null default now(),
    aruco_id bigint null,
    type text null,
    constraint machine_pkey primary key (machine_id),
    constraint public_machine_aruco_id_fkey foreign key (aruco_id) references aruco_tag (aruco_id) on update cascade on delete cascade,
    constraint public_machine_workstation_id_fkey foreign key (workstation_id) references workstation (workstation_id) on update cascade on delete cascade
  ) tablespace pg_default;

  create table
  public.tray (
    tray_id bigint generated by default as identity,
    workstation_id bigint null,
    created_at timestamp with time zone not null default now(),
    type text null,
    aruco_id bigint null,
    constraint trays_pkey primary key (tray_id),
    constraint public_trays_workstation_id_fkey foreign key (workstation_id) references workstation (workstation_id) on update cascade on delete cascade,
    constraint public_tray_aruco_id_fkey foreign key (aruco_id) references aruco_tag (aruco_id) on update cascade on delete cascade
  ) tablespace pg_default;

  create table
  public.tray_slot (
    slot_id bigint generated by default as identity,
    tray_id bigint null,
    created_at timestamp with time zone not null default now(),
    name text null,
    constraint tray_slot_pkey primary key (slot_id),
    constraint public_tray_slot_tray_id_fkey foreign key (tray_id) references tray (tray_id) on update cascade on delete cascade
  ) tablespace pg_default;

  create table
  public.vessel (
    vessel_id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    name text null,
    constraint vessel_pkey primary key (vessel_id),
    constraint Vessel_name_key unique (name)
  ) tablespace pg_default;

create table
  public.vessel_placements (
    vessel_id bigint generated by default as identity,
    slot_id bigint not null,
    created_at timestamp with time zone not null default now(),
    constraint vessel_placements_pkey primary key (vessel_id, slot_id),
    constraint public_vessel_placements_vessel_id_fkey foreign key (vessel_id) references vessel (vessel_id) on update cascade on delete cascade,
    constraint public_vessel_placements_slot_id_fkey foreign key (slot_id) references tray_slot (slot_id) on update cascade on delete cascade
  ) tablespace pg_default;

create table
  public.workstation (
    workstation_id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    name text null,
    lookout_pose json null,
    constraint workstation_pkey primary key (workstation_id)
  ) tablespace pg_default;